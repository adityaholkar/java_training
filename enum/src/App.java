public class App {
    public static void main(String[] args) throws Exception {
        Number number = Number.ONE;
        switch(number){
            case ONE:
                System.out.println("One");
                break;
            case TWO:
                System.out.println("Two");
                break;
            case Three:
            System.out.println("Three");
                break;
            default:
                break;

        }
    }
}
